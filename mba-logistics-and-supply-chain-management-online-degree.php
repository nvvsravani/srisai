		<p class="margin-top-25px text-grey-5 font-weight-500" style="font-size: 15px; line-height: 1.8;">Consider a customer walking into an apparel store to buy shirts. Here, the supply chain starts with the customer and his/her need for shirts. The next stage of the supply chain is the apparel store which stocks shirts and a variety of other apparel. The store stocks shirts supplied by a wholesaler. The wholesaler in turn would have procured shirts from several manufacturers. These manufacturers would have sourced raw materials and other supplies from several traders spread across the geography and this chain continues further.</p>
		<p class="margin-top-25px text-grey-5 font-weight-500" style="font-size: 15px; line-height: 1.8;">Logistics and Supply Chain Management (LSCM) is aptly called Demand Network Management as it operates on the pull from the customer and there is a network of suppliers, manufacturers, distributors, retailers, transporters, warehouse owners, and many more intermediaries. LSCM implies a well-organized and managed procurement function. LSCM plays an important role in moving items quickly and efficiently to destinations. LSCM is an integral part of most businesses and is vital for a company’s success and customer satisfaction. LSCM has become all the more important on account of global sourcing, omnichannel distribution, and widespread markets. Regardless of whether one wishes to become an entrepreneur or work in a corporate setup, one has to have a good knowledge of how a supply chain operates.</p>
